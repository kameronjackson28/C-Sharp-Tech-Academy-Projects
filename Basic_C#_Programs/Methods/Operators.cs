using System;
using System.Collections.Generic;
using System.Text;

namespace Methods
{
    class Operators
    {
        //creates method named multiply
        public int multiply(int x)
        {
            // returns 5 * x which is userNum
            return (5 * x);
        }

        //creates method named subtract
        public int subtract(int x)
        {
            //returns 7 - x which is userNum
            return (7 - x);
        }

        //creates method named add
        public int add(int x)
        {
            //returns 5+x which is userNum
            return (5 + x);
        }
    }
}
